import React from 'react';
import { Link } from 'react-router-dom';



const SmallCategoryNames = ({ basePath, categories, colors }) => {
    console.log("colors", colors)
  return (
    <>
      {categories.map((category) => {
        const subCats = category.subcategories.length > 0 ? <SmallCategoryNames basePath={category.Name+"/"} categories={category.subcategories} colors={colors} /> : null;

        if (category.resources.length > 0) {
            return (
             
                <>
                     <div style={{display: 'flex', alignItems: 'center', gap: '2px'}}>
                    <div style={{ height: "15px", width: "15px", background: colors[category.ID] }}> </div>
              <Link to={`${basePath}${category.Name}`} style={{ marginRight: '20px', fontWeight: 'bold' }}>
                {category.Name}
              </Link> </div>
                        {subCats}
                       
    </>
          );
        } else {
          return subCats;
        }
      })}
    </>
  );
};



export default SmallCategoryNames;